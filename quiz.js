let quiz_buffer = {};

let start_game = document.getElementById('start_game');

let next_game = document.getElementById("next_game");

next_game.style.display = 'none';

let quiz_game = document.getElementById('quiz_game');
start_game.addEventListener('click',event=>{

    let restart_game = document.createElement('a');
    restart_game.textContent = 'Restart Game';
    restart_game.setAttribute('id','restart_game');
    restart_game.setAttribute('href','quiz.html');
    (quiz_game.parentNode).appendChild(restart_game);
    restart_game.style.display = 'none';





    let random= Math.floor((Math.random())* (quiz.length -1 ))+1;
    console.log(random);
    let h1 = document.createElement('h1');
    h1.textContent = quiz[random]["question"];
    h1.setAttribute( "id", "quiz_question");

    quiz_game.appendChild(h1);
    let choose = ["A",'B',"C","D"];
    for (let i = 0;i<4;i++){
        let button = document.createElement("button");
        button.textContent = quiz[random]["options"][i][choose[i]];
        quiz_game.appendChild(button);
        // console.log(button);
        button.setAttribute('id','button'+i);

    }
    quiz_buffer[random]  = 1;
    start_game.style.display = 'none';
    // next_game.style.display = 'block';
    let a_tag = document.getElementById('a_tag');
    a_tag.style.marginLeft = "80%";
    // console.log(a_tag);
    a_tag.style.marginTop = '0';
    next_game.style.marginTop = '35%';

    let option_button = document.querySelectorAll('#quiz_game button');

    for(let i = 0;i<4;i++){
        console.log(option_button[i]);
        option_button[i].addEventListener('click',event=>{
            // console.log(random);
            // console.log(option_button[i].textContent);
            // console.log(quiz[random]["Answer"]);
            if(option_button[i].textContent === quiz[random]["Answer"]){
                console.log("it's correct !!!");
                option_button[i].style.backgroundColor = '#2ecc71';
                count ++;
                console.log(count);
                option_button[i].disabled = true;
                next_game.style.display = 'block';
            }
            else {

                option_button[i].style.backgroundColor = 'red';
                console.log("count  --->"+ count);
                next_game.style.display = 'block';

            }
        });
    }





});


